for (let i = 1; i <= square_numbers; i++) {
    const square = document.createElement("div")
    square.setAttribute('class', 'square')
    square.addEventListener('mouseover', () => {
        setColor(square)
    })
    square.addEventListener('mouseleave', () => {
        removeColor(square)
    })
    on.addEventListener("click", ()=>{
        square.style.background = randomColor()
        body.style.background = randomColor()
    })
    off.addEventListener('click', ()=>{
        removeColor(square)
        body.style.background = '#000000'
    })
    board.append(square)
}

function setColor(item) {
    const color = randomColor()
    item.style.transform = 'scale(1.4)'
    item.style.background = color

    item.style.boxShadow = `0 0 2px ${color}, 0 0 10px ${color}`
}

function randomColor() {
    const idx = Math.floor(Math.random() * CSS_COLOR_NAMES.length)
    return CSS_COLOR_NAMES[idx]
}

function removeColor(item) {
    item.style.background = "#1d1d1d"
    item.style.boxShadow = '0 0 2px #000'
    item.style.transform = 'scale(1)'
}
